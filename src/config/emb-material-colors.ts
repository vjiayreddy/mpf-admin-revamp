export type EmbMaterialColorOption = {
  _id: string
  color?: string
  colorname: string
  label: string
}

/** Alias with corrected spelling for new code. */
export const EMB_MATERIAL_COLORS = EMB_METERIAL_COLORS as Record<
  string,
  EmbMaterialColorOption[]
>

/** Legacy key: lowercase material name with spaces → underscores. */
export function materialColorKey(materialName?: string | null) {
  const raw = (materialName || "").trim().toLowerCase()
  if (!raw) return ""
  return raw.replace(/\s+/g, "_")
}

export function colorsForMaterial(
  materialName?: string | null
): EmbMaterialColorOption[] {
  const key = materialColorKey(materialName)
  if (!key) return []
  return EMB_MATERIAL_COLORS[key] ?? []
}
