import type { AnalyticsItem } from "@/lib/apollo/queries/analytics"

function labelOf(item: AnalyticsItem) {
  return item.label?.toLowerCase() ?? ""
}

export function filterByIncludes(
  items: AnalyticsItem[],
  key: string
): AnalyticsItem[] {
  const needle = key.toLowerCase()
  return items.filter((item) => labelOf(item).includes(needle))
}

export function partitionUsers(items: AnalyticsItem[]) {
  const filterUserItems = (key: string) => {
    if (key === "Non Customer Stylist:") {
      return items.filter((item) => {
        const label = labelOf(item)
        return (
          label.includes("non customer stylist") ||
          label === "without non customer stylist"
        )
      })
    }
    return filterByIncludes(items, key)
  }

  return {
    totalCustomers: filterUserItems("Customers"),
    totalNonCustomers: filterUserItems("Non Customer"),
    totalCount: filterUserItems("Total Count"),
    statusCustomers: filterUserItems("Status:"),
    statusNonCustomers: filterUserItems("N/C Status:"),
    customersStylist: filterUserItems("Customer Stylist:"),
    nonCustomersStylist: filterUserItems("Non Customer Stylist:"),
    withOutCCmissed: filterUserItems("CC Missed"),
    withOutCCToday: filterUserItems("CC Today"),
  }
}

export function partitionLeads(items: AnalyticsItem[]) {
  const filterLeadsItems = (key: string) => {
    const normalizedKey = key.trim().toLowerCase()
    return items.filter((item) => {
      const label = labelOf(item)
      if (!label) return false
      if (normalizedKey === "stylist:") {
        return label.startsWith("stylist:") || label === "without stylist"
      }
      if (normalizedKey === "generated by stylist:") {
        return (
          label.startsWith("generated by stylist:") ||
          label === "without generated by stylist"
        )
      }
      if (normalizedKey === "follow up") {
        return (
          label.includes("followup missed") || label.includes("followup today")
        )
      }
      return label.includes(normalizedKey)
    })
  }

  return {
    totalLeads: filterLeadsItems("Leads"),
    sourceLeads: filterLeadsItems("Source:"),
    statusLeads: filterLeadsItems("Status:"),
    stylistScheduled: filterLeadsItems("Stylist:"),
    followUpLeads: filterLeadsItems("Follow Up"),
    occasionLead: filterLeadsItems("Occasion:"),
    generatedByLead: filterLeadsItems("Generated By Stylist:"),
  }
}

export function partitionCif(items: AnalyticsItem[]) {
  const filterCifItems = (key: string) =>
    items.filter((item) => {
      const label = labelOf(item)
      if (key === "Stylist:") {
        return label.startsWith("stylist:") || label === "without stylist"
      }
      if (key === "Studio:") return label.startsWith("studio:")
      if (key === "Occasion:") {
        return label.startsWith("occasion:") || label === "without occasion"
      }
      if (key === "Source:") {
        return label.startsWith("source:") || label === "without source"
      }
      if (key === "Follow Up") {
        return (
          label.includes("followup missed") || label.includes("followup today")
        )
      }
      return label.includes(key.toLowerCase())
    })

  return {
    allCifAnalytics: items.filter((item) => item.label !== "CIF"),
    stylistCif: filterCifItems("Stylist:"),
    statusCif: filterCifItems("Status:"),
    followUpCif: filterCifItems("Follow Up"),
    occasionCif: filterCifItems("Occasion:"),
    sourceCif: filterCifItems("Source:"),
    studioOrdersCif: filterCifItems("Studio:"),
  }
}

