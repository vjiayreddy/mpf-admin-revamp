export function partitionEmb(items: AnalyticsItem[]) {
  const filterOrderItems = (key: string) =>
    items.filter((item) => {
      if (!item?.label) return false
      if (
        key === "Status:" &&
        (item.label.startsWith("Status:") || item.label === "Without Status")
      ) {
        return true
      }
      if (
        key === "Marking Status:" &&
        (item.label.startsWith("Marking Status:") ||
          item.label === "Without Marking Status")
      ) {
        return true
      }
      if (
        key === "Approval Status:" &&
        (item.label.startsWith("Approval Status:") ||
          item.label === "Without Approval Status")
      ) {
        return true
      }
      if (
        key === "Product:" &&
        (item.label.startsWith("Product:") || item.label === "Without Product")
      ) {
        return true
      }
      if (
        key === "Work Type:" &&
        (item.label.startsWith("Work Type:") ||
          item.label === "Without Work Type")
      ) {
        return true
      }
      if (
        key === "Duration Range:" &&
        item.label.startsWith("Duration Range:")
      ) {
        return true
      }
      if (
        (key === "Total Orders with EMB" ||
          key === "Total Embroidery Amount" ||
          key === "Zero Value Embroideries" ||
          key === "Average Duration") &&
        item.label === key
      ) {
        return true
      }
      return false
    })

  return {
    embWorkTypeOrders: filterOrderItems("Work Type:"),
    embMarkingStatusOrders: filterOrderItems("Marking Status:"),
    embStatusOrders: filterOrderItems("Status:"),
    embApprovalStatusOrders: filterOrderItems("Approval Status:"),
    productEmbOrders: filterOrderItems("Product:"),
    totalEmbCount: filterOrderItems("Total Orders with EMB"),
    totalEmbAmount: filterOrderItems("Total Embroidery Amount"),
    zeroValueEmb: filterOrderItems("Zero Value Embroideries"),
    embDurationRangeOrders: filterOrderItems("Duration Range:"),
    avgDuration: filterOrderItems("Average Duration"),
  }
}

