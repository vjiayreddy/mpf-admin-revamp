export function colorForLabel(label: string): string {
  const palette = [
    "oklch(0.55 0.18 250)",
    "oklch(0.6 0.16 145)",
    "oklch(0.65 0.15 85)",
    "oklch(0.58 0.18 25)",
    "oklch(0.55 0.14 310)",
    "oklch(0.5 0.12 200)",
    "oklch(0.62 0.14 40)",
    "oklch(0.48 0.1 280)",
  ]
  let hash = 0
  for (let i = 0; i < label.length; i++) {
    hash = (hash * 31 + label.charCodeAt(i)) >>> 0
  }
  return palette[hash % palette.length]
}
